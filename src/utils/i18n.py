def _load_resource_translations():
    resource_dir = Path(__file__).resolve().parents[1] / "resources" / "i18n"
    for language in LANGUAGES:
        path = resource_dir / f"{language}.json"
        if not path.is_file():
            continue
        try:
            data = json.loads(path.read_text(encoding="utf-8"))
        except (OSError, json.JSONDecodeError):
            continue
        if isinstance(data, dict):
            _TRANSLATIONS.setdefault(language, {}).update({str(k): str(v) for k, v in data.items()})


_load_resource_translations()

_current_language = "en"


def set_language(language: str) -> str:
    global _current_language
    if language not in LANGUAGES:
        language = "en"
    _current_language = language
    return _current_language


def get_language() -> str:
    return _current_language


def tr(text: str, language: str | None = None) -> str:
    lang = language or _current_language
    if lang == "en":
        return text
    return _TRANSLATIONS.get(lang, {}).get(text, text)


def tr_model_display(model_key: str, fallback: str) -> str:
    names = {
        "zh_CN": {
            "birefnet": "BiRefNet（最佳质量）",
            "birefnet_portrait": "BiRefNet 人像（人物）",
            "modnet": "人像模式",
        }
    }
    return names.get(_current_language, {}).get(model_key, fallback)


def tr_model_description(description: str) -> str:
    return tr(description)
